void run_motor(uint8_t motor_number, float degree, uint8_t direction)
{
	int total_pulses;
  switch(motor_number)
  {
    case 1 : total_pulses = 0;
              break;
    case 2 : total_pulses = 0;
              break;
    case 3 : total_pulses = 0;
              break;
    case 4 : total_pulses = 6400;
              direction ? HAL_GPIO_WritePin(DIR4_GPIO_Port, DIR4_Pin, GPIO_PIN_SET) : HAL_GPIO_WritePin(DIR4_GPIO_Port, DIR4_Pin, GPIO_PIN_RESET);
              break;          
    case 5 : total_pulses = 0;
              break;
    case 6 : total_pulses = 8000;
              direction ? HAL_GPIO_WritePin(GPIOA, DIR6_Pin, GPIO_PIN_SET) : HAL_GPIO_WritePin(GPIOA, DIR6_Pin, GPIO_PIN_RESET);
              break;
    default : total_pulses = 0;
              break;
  }
  float pulses_per_degree = total_pulses / 360.0;  // Pulses per degree (17.777...)

  int whole_pulses = degree * pulses_per_degree;   // Whole number of pulses to move
  float remainder = (degree * pulses_per_degree) - whole_pulses;  // Fractional remainder

  static float accumulated_remainder = 0.0;  // Accumulated fractional part

    // Add remainder to accumulated error
    accumulated_remainder += remainder;

    if (accumulated_remainder >= 1.0) {
        whole_pulses += 1;                    // Add an extra pulse when the remainder >= 1
        accumulated_remainder -= 1.0;         // Subtract 1 from the accumulated remainder
    }
  
  /* Send pulses to step pin */
  switch(motor_number)
  {
    case 1 : 
              break;
    case 2 : 
              break;
    case 3 : 
              break;
    case 4 : for (uint16_t i = 0; i < whole_pulses; i++)
              {
                HAL_GPIO_WritePin(GPIOA, PUL4_Pin, GPIO_PIN_SET);  // Step High
                delay_us(100);  // Delay (adjust if you want faster/slower speed)
                HAL_GPIO_WritePin(GPIOA, PUL4_Pin, GPIO_PIN_RESET);  // Step Low
                delay_us(100);  // Delay between steps
              }
              break;
    case 5 : 
              break;
    case 6 :  for (uint16_t i = 0; i < whole_pulses; i++)
              {
                HAL_GPIO_WritePin(PUL6_GPIO_Port, PUL6_Pin, GPIO_PIN_SET);  // Step High
                delay_us(100);  // Delay (adjust if you want faster/slower speed)
                HAL_GPIO_WritePin(PUL6_GPIO_Port, PUL6_Pin, GPIO_PIN_RESET);  // Step Low
                delay_us(100);  // Delay between steps
              }
              break;
    default : 
              break;
  }
}

